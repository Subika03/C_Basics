#include<stdio.h>

int main()
{
    int day=5;

    switch(day)
    {
        case 0:
        printf("Monday");
        break;
        case 1:
        printf("Tuesday");
        break;
        case 3:
        printf("Wednesday");
        break;
        case 4:
        printf("Thursday");
        break;
        default:
        printf("Saturday");

    }
}